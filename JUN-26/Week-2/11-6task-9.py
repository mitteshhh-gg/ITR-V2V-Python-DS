# VARIOUS FUNTION OF SETS
# creation of set
nums = {1,2,3}
print("\nSet : ",nums)

# adding element
nums.add(4)
print("\nAfter Adding element : ",nums)

# removing element 
nums.remove(4)
print("\nAfter Removing elements : ",nums)

# membership operator
print("\nCheking if 2 is present in the set using membership op (in) : ",2 in nums)
